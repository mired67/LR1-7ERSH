def task_3_6():
    print("Задание 3.6. Следующее и предыдущее число")
    n = int(input("Введите число: "))
    print("Следующее число:", n + 1)
    print("Предыдущее число:", n - 1)

def task_3_7():
    print("Задание 3.7. Парты")
    a = int(input("Введите количество учеников в 1 классе: "))
    b = int(input("Введите количество учеников во 2 классе: "))
    c = int(input("Введите количество учеников в 3 классе: "))
    print("Минимальное количество парт:", (a + 1) // 2 + (b + 1) // 2 + (c + 1) // 2)

def task_3_8():
    print("Задание 3.8. Шнурки")
    a = int(input("Введите расстояние между рядами: "))
    b = int(input("Введите расстояние между дырочками: "))
    l = int(input("Введите длину свободного конца: "))
    N = int(input("Введите количество дырочек в одном ряду: "))
    print("Длина шнурка:", 2 * l + (2 * N - 1) * a + 2 * (N - 1) * b)

