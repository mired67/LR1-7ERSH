def task_3_1():
    print("Задание 3.1. Сумма трех чисел")
    a = int(input("Введите первое число: "))
    b = int(input("Введите второе число: "))
    c = int(input("Введите третье число: "))
    print("Сумма трех чисел:", a + b + c)

def task_3_2():
    print("Задание 3.2. Площадь прямоугольного треугольника")
    b = int(input("Введите основание: "))
    h = int(input("Введите высоту: "))
    print("Площадь треугольника:", b * h / 2)

def task_3_3():
    print("Задание 3.3. Апельсины в коробке")
    x = int(input("Введите количество школьников: "))
    y = int(input("Введите количество апельсинов: "))
    print("Каждому школьнику достанется:", y // x)
    print("Останется апельсинов:", y % x)

def task_3_4():
    print("Задание 3.4. Часы")
    m = int(input("Введите количество минут: "))
    print("Часы и минуты:", (m // 60) % 24, "ч.", m % 60, "мин.")

